#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	char	*t;
	size_t	i;

	t = (char *)s;
	i = 0;
	while (i < n)
	{
		if (t[i] == (char)c)
			return ((void *)s + i);
		i++;
	}
	return (NULL);
}
